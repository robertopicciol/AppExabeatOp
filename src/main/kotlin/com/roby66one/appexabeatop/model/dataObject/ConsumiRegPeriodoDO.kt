package com.roby66one.appexabeatop.model.dataObject

import com.roby66one.appexabeatop.model.businessObject.consumiRegPeriodo.ConsumiRegPeriodoBO
import java.sql.Timestamp


data class ConsumiRegPeriodoDO (
        val anno_mese : Long,
        val id_punto_misura : Long,
        val da_giorno : Long,
        val a_giorno : Long,
        val cons_et1 : Double?,
        val cons_et2 : Double?,
