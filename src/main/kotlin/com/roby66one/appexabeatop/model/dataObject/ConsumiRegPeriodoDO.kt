                this.picco_wt3,
                this.picco_wt4,
                this.provenienza,
                this.fascia_attendibilita,
                this.tipo_cons_et1,
                this.tipo_cons_et2,
                this.tipo_cons_et3,
                this.tipo_cons_et4,
                this.codice_mis,
                this.matricola_mis,
                this.day_min_let,
                this.day_max_let,
                this.dt_inizio_val.toLocalDateTime(),
                this.dt_fine_val.toLocalDateTime(),
                this.dt_crea.toLocalDateTime(),
                this.aut_crea,
                this.dt_mod,
                this.aut_mod,
                this.flag_lettura_lavoro
